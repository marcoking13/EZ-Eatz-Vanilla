function renderElement(tag,text,classes,append,data){

var element =  $(tag).text(text).appendTo(append);
  for(var i =0; i<classes.length;i++){
    element.addClass(classes);

  }
  for(var i =0; i<data.length;i++){
    element.attr("data"+i,data);
  }
}

